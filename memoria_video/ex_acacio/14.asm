PILHA SEGMENT PARA STACK 'STACK'
    DB 1024 DUP('PILHA')
PILHA ENDS

DADOS SEGMENT PARA 'DATA'
DADOS ENDS

CODIGO SEGMENT PARA 'CODE'

    ASSUME CS:CODIGO, DS:DADOS, SS:PILHA

INICIO:
    MOV AX, DADOS
    MOV DS, AX

    MOV AX, 0B800H
    MOV ES, AX

    MOV DI, 0000H
    MOV CX, 40*25
    MOV AL, ' '

REPETE:
    MOV AH, 11110000B
    MOV ES:[DI], AL
    MOV ES:[DI+1], AH
    ADD DI, 2

    MOV AH, 00001111B
    MOV ES:[DI], AL
    MOV ES:[DI+1], AH
    ADD DI, 2

    LOOP REPETE

    MOV AH, 01H
    INT 21H

FIM:
    MOV AH, 4CH
    INT 21H

CODIGO ENDS
END INICIO